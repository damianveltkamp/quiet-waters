// One-time / regeneration tool. Reads the source photos, writes phone-sized
// WebP into assets/backgrounds/, and generates a typed asset module with a
// literal require() + dominant fallback color per image.
import { readdirSync, mkdirSync, writeFileSync } from 'node:fs';
import { join, extname } from 'node:path';
import { homedir } from 'node:os';
import sharp from 'sharp';

const SRC = process.env.BG_SRC ?? join(homedir(), 'Documents', 'bgs');
const OUT_DIR = join(process.cwd(), 'assets', 'backgrounds');
const GEN = join(process.cwd(), 'src', 'features', 'wallpaper', 'backgroundImages.generated.ts');

const numeric = (name) => parseInt(name.replace(/\D/g, ''), 10) || 0;
const files = readdirSync(SRC)
  .filter((f) => ['.jpg', '.jpeg', '.png'].includes(extname(f).toLowerCase()))
  .sort((a, b) => numeric(a) - numeric(b));

mkdirSync(OUT_DIR, { recursive: true });
const hex = (c) => '#' + [c.r, c.g, c.b].map((n) => n.toString(16).padStart(2, '0')).join('');

const entries = [];
let i = 0;
for (const file of files) {
  i += 1;
  const id = `scene-${String(i).padStart(2, '0')}`;
  const img = sharp(join(SRC, file)).rotate();
  await img
    .clone()
    .resize({ width: 1290, height: 2796, fit: 'cover' })
    .webp({ quality: 80 })
    .toFile(join(OUT_DIR, `${id}.webp`));
  const { dominant } = await img.stats();
  entries.push({ id, fallbackColor: hex(dominant) });
}

const body = entries
  .map((e) => `  { id: '${e.id}', source: require('../../../assets/backgrounds/${e.id}.webp'), fallbackColor: '${e.fallbackColor}' },`)
  .join('\n');

writeFileSync(
  GEN,
  `// AUTO-GENERATED by scripts/process-backgrounds.mjs — do not edit by hand.\n` +
    `import type { ImageSourcePropType } from 'react-native';\n\n` +
    `export interface ImageAsset { id: string; source: ImageSourcePropType; fallbackColor: string }\n\n` +
    `export const IMAGE_ASSETS: readonly ImageAsset[] = [\n${body}\n];\n`,
);

console.log(`Wrote ${entries.length} images to ${OUT_DIR} and ${GEN}`);
